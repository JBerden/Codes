#include <stdio.h>
#include "pqSorted.h"

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

void clear( PQ *pq )
{
  pq->count = 0;
}

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

bool isEmpty( PQ *pq )
{
  if ( pq->count == 0 )
  {
    return true;
  }
  else
  {
    return false;
  }
}

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

void put( PQ *pq , ITEM item )
{
  int i_item;
  int i=0;

  for ( i = 0 ; i < pq->count ; i++ )
  {
    if ( item.prio > pq->items[i].prio )
    {
      break;
    }
  }

  // move all items

  int iInsert = i;

  for ( i = pq->count ; i > iInsert ; i-- )
  {
    pq->items[i] = pq->items[i-1];
  }

  pq->items[iInsert] = item;

  pq->count++;
}

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------

ITEM getPrio( PQ *pq )
{
  pq->count = pq->count - 1;  // lower counter

  return pq->items[ pq->count ];
}

